Rank = int  # 1..13, where 1 = Ace, 11-13 = J,Q,K

# ---------- Utilities ----------
RANKS = list(range(1, 14))
RANK_TO_VALUE = {r: min(r, 10) for r in RANKS}  # 1->1 (Ace treated specially), 11-13 -> 10


def card_str(rank: Rank) -> str:
    if rank == 1: return 'A'
    if rank == 11: return 'J'
    if rank == 12: return 'Q'
    if rank == 13: return 'K'
    return str(rank)